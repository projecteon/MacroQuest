local function testSpawnMembers()
  mq.cmd.target() -- Set target to self
  for _key, member in pairs(spawnMembers) do
    print('Checking mq.TLO.Target.' .. member, ' - ', mq.TLO.Target[member])
  end
end

local function testCharacterMembers()
  for _key, member in pairs(characterMembers) do
    print('Checking mq.TLO.Me.' .. member, ' - ', mq.TLO.Me[member])
  end
end

local function testTargetMembers()
  mq.cmd.target() -- Set target to self
  for _key, member in pairs(characterMembers) do
    print('Checking mq.TLO.Target.' .. member, ' - ', mq.TLO.Target[member])
  end
end

if args[1] == 'spawn' then
  testSpawnMembers()
elseif args[1] == 'character' then
  testCharacterMembers()
elseif args[1] == 'target' then
  testTargetMembers()
else
  print('Usage: /lua run dataMemberCheck spawn | character | target')
end